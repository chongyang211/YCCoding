#include <iostream>
using namespace std;
int main() {
    cout << "short 类型所占内存空间为： " << sizeof(short) << endl;
    cout << "int 类型所占内存空间为： " << sizeof(int) << endl;
    cout << "long 类型所占内存空间为： " << sizeof(long) << endl;
    cout << "long long 类型所占内存空间为： " << sizeof(long long) << endl;
    return 0;
}
//short 类型所占内存空间为： 2
//int 类型所占内存空间为： 4
//long 类型所占内存空间为： 8
//long long 类型所占内存空间为： 8
